package com.indigo.smarttravel.websocket;

import java.util.List;

public class SeatUpdateMessage {
    private Long flightId;
    private List<String> selectedSeats;
    private List<String> availableSeats;
    private String action; // "SELECT", "DESELECT", "CONFIRM", "RELEASE"
    private String userId;
    private long timestamp;

    public SeatUpdateMessage() {
        this.timestamp = System.currentTimeMillis();
    }

    public Long getFlightId() {
        return flightId;
    }

    public void setFlightId(Long flightId) {
        this.flightId = flightId;
    }

    public List<String> getSelectedSeats() {
        return selectedSeats;
    }

    public void setSelectedSeats(List<String> selectedSeats) {
        this.selectedSeats = selectedSeats;
    }

    public List<String> getAvailableSeats() {
        return availableSeats;
    }

    public void setAvailableSeats(List<String> availableSeats) {
        this.availableSeats = availableSeats;
    }

    public String getAction() {
        return action;
    }

    public void setAction(String action) {
        this.action = action;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public long getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(long timestamp) {
        this.timestamp = timestamp;
    }
}
